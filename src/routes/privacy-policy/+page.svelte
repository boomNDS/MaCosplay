		<li>ยืนยันตัวตนของผู้ใช้และเปิดใช้ฟีเจอร์ทางสังคม</li>
		<li>ตอบข้อสงสัยของลูกค้าและให้การสนับสนุน</li>
		<li>ปฏิบัติตามข้อกำหนดทางกฎหมายและบังคับใช้เงื่อนไขของเรา</li>
	</ul>

