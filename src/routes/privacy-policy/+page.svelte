		<li>หน่วยงานทางกฎหมาย: หากกฎหมายกำหนด เราอาจเปิดเผยข้อมูลเพื่อปฏิบัติตามคำร้องทางกฎหมาย</li>
		<li>การโอนธุรกิจ: ในกรณีที่มีการควบรวม กิจการ หรือการขายทรัพย์สิน ข้อมูลของคุณอาจถูกโอนย้าย</li>
	</ul>

